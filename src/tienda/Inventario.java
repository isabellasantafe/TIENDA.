    private void botonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarActionPerformed
        Tienda tienda = new Tienda("", "", "", 0, 0, true);
        
        tienda.setDuenoDeLaTienda(campoDueño.getText());
        campoDueño.setText("");
        
        tienda.setProductos(campoProducto.getText());
        campoProducto.setText("");
        
        tienda.setPersonal((Integer)campoPersonal.getValue());
        campoPersonal.setValue(0);
        
        tienda.setBarrio(campoBarrio.getText());
        campoBarrio.setText("");
        
        llenarTabla(tienda);
    }//GEN-LAST:event_botonIngresarActionPerformed

