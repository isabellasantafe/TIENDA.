    private void llenarTabla(Tienda tienda){
        DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
        
        Vector fila = new Vector();
        
        fila.add(tienda.getDuenoDeLaTienda());
        fila.add(tienda.getProductos());
        fila.add(tienda.getPersonal());
        fila.add(tienda.getBarrio());
        
        modelo.addRow(fila);
        
        JOptionPane.showMessageDialog(this, "Registro exitosamente realizado!");
    }
